import express,{ Router, Response, Request } from "express"
import { Encuesta1 } from "../../models/encuesta1.model";
import responseModule from "../../modules/response.module";
import encuesta1Controller from "./encuesta1.controller";

const router: Router = express.Router();

router.get("/all", async function(req: Request, res: Response){

    try {
        const result: Encuesta1[] = await encuesta1Controller.getEncuesta1();
        responseModule.success(req,res,result);

    } catch (error) {
        responseModule.error(req,res,"Error desconocido");
    }

});

router.get("/:id", async function(req: Request, res: Response){
    const id: string = req.params['id'];
    try {
        const result: Encuesta1 | null = await encuesta1Controller.getEncuesta1ById(id);
        responseModule.success(req,res,result);

    } catch (error) {
        console.log(error);
        responseModule.error(req,res,"Error desconocido");
    }
});

router.post("/add", async function(req: Request, res: Response){

    const body: Encuesta1 = req.body;
    try {
        const result: Encuesta1 = await encuesta1Controller.addEncuesta1(body);
        responseModule.success(req,res,result,201);    
    } catch (error) {
        responseModule.error(req,res,"Error desconocido");
    }
});


router.delete("/delete", async function (req: Request, res: Response) {
    try {
      const ver = await encuesta1Controller.deleteEncuesta1(req.body._id);
      if (ver != null) {
        responseModule.success(req, res, "Delete Success", 200);
      } else {
        responseModule.success(req, res, "Delete Failed");
      }
    } catch (error) {
      responseModule.error(req, res, "Error desconocido");
    }
  });

router.patch("/:id", async function(req: Request, res: Response){
    const { id } = req.params;
    try {
        const modPaciente = await encuesta1Controller.patchEncuesta1(id, req.body);
        responseModule.success(req,res,modPaciente,201);    
    } catch (error) {
        console.log(id);
        responseModule.error(req,res,"Error desconocido");
    }
});

export default router;